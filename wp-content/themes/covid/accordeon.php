La maladie à virus Ebola
L’hépatite B
La grippe
L’encéphalite japonaise
La rougeole
La méningite
Les oreillons
La coqueluche
La pneumonie
La poliomyélite
La rage
Les infections à rotavirus
La rubéole
Le tétanos
La typhoïde
La varicelle
La fièvre jaune
D’autres vaccins sont en cours de mise au point ou au stade des essais pilotes, notamment ceux contre le virus Zika ou le paludisme, mais ils ne sont pas encore couramment disponibles dans le monde.
Toutes ces vaccinations ne sont pas forcément nécessaires dans votre pays. Certaines ne seront administrées qu’avant un voyage dans des zones à risque ou à des personnes exerçant des professions très exposées. Parlez-en à votre agent de santé pour savoir de quelles vaccinations vous et votre famille avez besoin.</div>
