      <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseOne" aria-expanded="false" aria-controls="flush-collapseOne">
        Qu'est ce que la vaccination ?
      </button>
    </h2>
    <div id="flush-collapseOne" class="accordion-collapse collapse" aria-labelledby="flush-headingOne" data-bs-parent="#accordionFlushExample">
      <div class="accordion-body">La vaccination est un moyen simple, sûr et efficace de vous protéger des maladies dangereuses, avant d’être en contact avec ces affections. Elle utilise les défenses naturelles de l’organisme pour créer une résistance à des infections spécifiques et renforcer le système immunitaire.

        Les vaccins stimulent le système immunitaire pour créer des anticorps, de la même manière que s’il était exposé à la maladie. Mais comme les vaccins ne renferment que des formes tuées ou atténuées des germes, virus ou bactéries, ils ne provoquent pas la maladie et n’exposent pas le sujet à des risques de complications.

        La plupart des vaccins sont administrés par injection, mais certains se prennent par voie orale ou par aérosol nasal.</div>
    </div>
  </div>
  <div class="accordion-item">
    <h2 class="accordion-header" id="flush-headingTwo">
      <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseTwo" aria-expanded="false" aria-controls="flush-collapseTwo">
        Comment agit un vaccin ?
      </button>
    </h2>
    <div id="flush-collapseTwo" class="accordion-collapse collapse" aria-labelledby="flush-headingTwo" data-bs-parent="#accordionFlushExample">
      <div class="accordion-body">Les vaccins réduisent le risque de contracter une maladie en travaillant sur les défenses naturelles de l’organisme pour établir une protection. C’est le système immunitaire qui réagit lorsqu’on se fait vacciner :

        • Il reconnaît le germe invasif, comme un virus ou une bactérie.
        • Il produit des anticorps. Ce sont des protéines produites naturellement par le système immunitaire pour combattre la maladie.
        • Il se rappelle de la maladie par la suite et de la manière de la combattre. Lorsqu’on est exposé plus tard au même germe, le système immunitaire peut le détruire rapidement, avant que la personne ne tombe malade.

        Les vaccins sont donc un moyen sûr et astucieux de produire une réponse immunitaire de l’organisme, sans provoquer de maladie.
        Le système immunitaire a de la mémoire. Une fois qu’il a été exposé à une ou plusieurs doses vaccinales, le sujet vacciné demeure protégé contre la maladie concernée pendant des années, des décennies, voire la vie entière. C’est la raison pour laquelle les vaccins sont si efficaces. Plutôt que de traiter le mal, ils permettent d’éviter de tomber malade. </div>
    </div>
  </div>
  <div class="accordion-item">
    <h2 class="accordion-header" id="flush-headingThree">
      <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseThree" aria-expanded="false" aria-controls="flush-collapseThree">
        Pourquoi dois-je me faire vacciner ?
      </button>
    </h2>
    <div id="flush-collapseThree" class="accordion-collapse collapse" aria-labelledby="flush-headingThree" data-bs-parent="#accordionFlushExample">
      <div class="accordion-body">Sans les vaccins, nous sommes exposés à de sérieux risques de maladies et d’incapacités dues à des infections comme la rougeole, la méningite, la pneumonie, le tétanos ou la poliomyélite. Nombre de ces maladies sont potentiellement mortelles. Selon les estimations de l’OMS, les vaccins réalisés dans l'enfance permettent à eux seuls de sauver plus de 4 millions de vies par an.

        Bien que certaines maladies soient devenues rares, leurs agents continuent de circuler dans certaines ou toutes les régions du monde. À l’heure actuelle, les maladies infectieuses peuvent facilement traverser les frontières et infecter toute personne qui n’est pas protégée.

        Les deux raisons essentielles pour se faire vacciner sont de se protéger soi-même et de protéger son entourage. Comme on ne peut pas vacciner certaines personnes, les très jeunes nourrissons, ceux qui sont gravement malades ou ceux qui ont certaines allergies par exemple, elles dépendent de la vaccination des autres pour être protégées des maladies à prévention vaccinale.</div>
    </div>
  </div>



  <div class="accordion-item">
    <h2 class="accordion-header" id="flush-headingFour">
      <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#flush-collapseFour" aria-expanded="false" aria-controls="flush-collapseFour">
        Quand dois-je me faire vacciner (ou faire vacciner mes enfants) ?
      </button>
    </h2>
    <div id="flush-collapseFour" class="accordion-collapse collapse" aria-labelledby="flush-headingFour" data-bs-parent="#accordionFlushExample">
      <div class="accordion-body">Les vaccins nous protègent tout au long de la vie, à partir de la naissance, dans l’enfance, l’adolescence et jusqu’à la vieillesse. Dans la plupart des pays, nous recevons une fiche ou un carnet de vaccination nous disant les vaccins que nous ou nos enfants avons eus et les dates des prochains vaccins ou rappels. Il est important de rester à jour de toutes les vaccinations.

En retardant les vaccinations, on risque de tomber gravement malade. Si on attend le moment où nous pensons que nous risquons d’être exposés à une maladie, comme lors d’une épidémie, il ne restera peut-être pas suffisamment de temps pour que le vaccin puisse agir ou pour administrer toutes les doses recommandées.

Si vous êtes en retard dans les vaccinations recommandées, que ce soit pour vous ou pour votre enfant, parlez-en à votre prestataire de soins de santé pour procéder à un rattrapage. </div>
    </div>
